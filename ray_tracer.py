class Sphere:
    def __init__(self, center, radius, color, reflection):
        self.center = np.array(center)
        self.radius = radius
        self.color = np.array(color) / 255.0 # Normalize color
        self.reflection = reflection

    def intersect(self, ray):
        oc = ray.origin - self.center
        a = np.dot(ray.direction, ray.direction)
        b = 2 * np.dot(oc, ray.direction)
        c = np.dot(oc, oc) - self.radius**2
        discriminant = b*b - 4*a*c

        if discriminant < 0:
            return None
        else:
            t0 = (-b - np.sqrt(discriminant)) / (2*a)
            t1 = (-b + np.sqrt(discriminant)) / (2*a)

            if t0 > 1e-6: # Check for positive t to avoid self-intersection
                t = t0
            elif t1 > 1e-6:
                t = t1
            else:
                return None

            hit_point = ray.origin + t * ray.direction
            normal = (hit_point - self.center) / self.radius
            return t, hit_point, normal

class Light:
    def __init__(self, position, intensity):
        self.position = np.array(position)
        self.intensity = intensity

class Camera:
    def __init__(self, position, look_at, fov, width, height):
        self.position = np.array(position)
        self.width = width
        self.height = height

        aspect_ratio = float(width) / height
        angle = np.tan(np.deg2rad(fov / 2.0))

        self.forward = (look_at - position)
        self.forward = self.forward / np.linalg.norm(self.forward)

        self.right = np.cross(self.forward, np.array([0, 1, 0])) # Assuming Y is up
        self.right = self.right / np.linalg.norm(self.right)
        if np.dot(self.right, self.right) == 0: # Handle straight up/down camera
             self.right = np.array([1, 0, 0])

        self.up = np.cross(self.right, self.forward)
        self.up = self.up / np.linalg.norm(self.up)

        self.plane_width = 2 * angle * aspect_ratio
        self.plane_height = 2 * angle

    def get_ray(self, x, y):
        u = (x + 0.5) / self.width
        v = (y + 0.5) / self.height

        x_coord = (2 * u - 1) * self.plane_width / 2.0
        y_coord = (1 - 2 * v) * self.plane_height / 2.0

        ray_direction = self.forward + x_coord * self.right + y_coord * self.up
        return Ray(self.position, ray_direction)

class Scene:
    def __init__(self, objects, lights):
        self.objects = objects
        self.lights = lights

