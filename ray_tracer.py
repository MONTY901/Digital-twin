import numpy as np
from renderer.vector import reflect
from renderer.ray import Ray

class Renderer:
    def __init__(self, scene, camera, max_depth=3):
        self.scene = scene
        self.camera = camera
        self.max_depth = max_depth

    def trace(self, ray, depth):
        if depth > self.max_depth:
            return np.array([0, 0, 0])

        hit_obj = None
        min_t = float("inf")
        hit_point = None
        normal = None

        # Find closest intersection
        for obj in self.scene.objects:
            result = obj.intersect(ray)
            if result:
                t, p, n = result
                if t < min_t:
                    min_t = t
                    hit_obj = obj
                    hit_point = p
                    normal = n

        # Background color
        if hit_obj is None:
            return np.array([0.2, 0.7, 1.0])

        view_dir = -ray.direction
        color = 0.1 * hit_obj.color  # Ambient

        for light in self.scene.lights:
            light_dir = light.position - hit_point
            light_dist = np.linalg.norm(light_dir)
            light_dir = light_dir / light_dist

            # 🔥 SHADOW CHECK
            shadow_ray = Ray(hit_point + 1e-5 * normal, light_dir)
            in_shadow = False

            for obj in self.scene.objects:
                shadow_hit = obj.intersect(shadow_ray)
                if shadow_hit:
                    t_shadow, _, _ = shadow_hit
                    if t_shadow < light_dist:
                        in_shadow = True
                        break

            if in_shadow:
                continue

            # Diffuse
            diff = max(np.dot(normal, light_dir), 0)
            diffuse = diff * hit_obj.color * light.intensity

            # Specular (Phong)
            reflect_dir = reflect(-light_dir, normal)
            spec = max(np.dot(view_dir, reflect_dir), 0) ** 32
            specular = spec * light.intensity * np.array([1, 1, 1])

            color += diffuse + specular

        # 🔁 REFLECTION
        reflect_dir = reflect(ray.direction, normal)
        reflect_ray = Ray(hit_point + 1e-5 * normal, reflect_dir)
        reflect_color = self.trace(reflect_ray, depth + 1)

        color = (1 - hit_obj.reflection) * color + hit_obj.reflection * reflect_color

        return np.clip(color, 0, 1)

    def render(self):
        width, height = self.camera.width, self.camera.height
        image = np.zeros((height, width, 3))

        for y in range(height):
            for x in range(width):
                ray = self.camera.get_ray(x, y)
                image[y, x] = self.trace(ray, 0)

        return image

